QString N::Acoustics::Russian(int category)
{
  QString Russian = QString::fromUtf8(QByteArray((const char *)RussianAlphabet,RussianAlphabetSize)) ;
  QString P                         ;
  switch (category)                 {
    case N::Speak::RussianCapital   :
      for (int i=0;i<33;i++)        {
        P.append(Russian.at(i))     ;
      }                             ;
    return P                        ;
    case N::Speak::RussianMinuscule :
      for (int i=45;i<78;i++)       {
        P.append(Russian.at(i))     ;
      }                             ;
    return P                        ;
  }                                 ;
  return Russian                    ;
}
